//干支对应年份的地目经信息
func Dmj(y, m, d int) string {
	jqarrOBj := NewJQArr(y)
	cust := time.Date(y, time.Month(m), d, 0, 0, 0, 0, time.Local)
	b, _ := jqarrOBj.LinChuT(cust)
	var g, z int
	switch b {
	case false: //false:日期在立春之前
		//干
		g = 1 + (y+6)%10
		if g -= 1; g < 1 {
			g += 10
		}

		//支
		z = 1 + (y+8)%12
		if z -= 1; z < 1 {
			z += 12
		}

	case true: //日期在立春之后
		g = 1 + (y+6)%10
		z = 1 + (y+8)%12
	}
	//fmt.Println("debug dgz", g, z)
	return info(g, z)
}

